void	ft_putchar(char c);

void	ft_is_negative(int n)
{
	char x;
	char y;

	x = 'N';
	y = 'P';
	if (n < 0)
	{
		ft_putchar(x);
	}
	else if (n >= 0)
	{
		ft_putchar(y);
	}
}
